'''CRIE UM PROGRAMA QUE LEIA UM NUMERO INTEIRO
E MOSTRE SE ELE É PAR OU IMPAR'''

num = int(input('DIGITE UM NÚMERO...:'))
if num % 2 == 0:
    print('O NÚMERO {} É PAR'.format(num))
else:
    print('O NUMERO {} É IMPAR'.format(num))
